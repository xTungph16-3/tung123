    ///Phần code này thực hiện phân trang cho spct
    private void preview() {
        // TODO add your handling code here:
        if (viTriTrang >= 2) {
            viTriTrang--;
            setSoTrang();
            tienLui -= 5;
            this.fillTBSanPhamChiTiet(spctDAO.phanTrangSPCT(SanPham_View.sP_id, tienLui));
        } else {
            last();
        }
    }

    private void next() {
        // TODO add your handling code here:

        if (viTriTrang < soTrang) {
            viTriTrang++;
            setSoTrang();
            tienLui += 5;
            this.fillTBSanPhamChiTiet(spctDAO.phanTrangSPCT(SanPham_View.sP_id, tienLui));
        } else {
            first();
        }
    }

    private void first() {
        viTriTrang = 1;
        setSoTrang();
        tienLui = 0;
        this.fillTBSanPhamChiTiet(spctDAO.phanTrangSPCT(SanPham_View.sP_id, tienLui));
    }

    private void last() {
        viTriTrang = soTrang;
        setSoTrang();
        tienLui = 5 * soTrang - 5;
        this.fillTBSanPhamChiTiet(spctDAO.phanTrangSPCT(SanPham_View.sP_id, tienLui));
    }

    private void setSoTrang() {
        soTrangLe = Math.ceil((double) spctDAO.selectSPTCByIDSP(SanPham_View.sP_id).size() / 5);
        soTrang = (int) soTrangLe;
        if (soTrang < 1) {
            soTrang += 1;
            lblSoTrangSPCT.setText(viTriTrang + "/" + soTrang);
        } else {
            lblSoTrangSPCT.setText(viTriTrang + "/" + soTrang);
        }
    }

